// Add active class to current element
let links = document.querySelectorAll(".navbar-nav a");

links.forEach((a) => {
  a.addEventListener("click", (a) => {
    links.forEach((a) => {
      a.classList.remove("active-link");
    });
    a.currentTarget.classList.add("active-link");
  });
});

